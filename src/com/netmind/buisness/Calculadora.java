package com.netmind.buisness;

public class Calculadora {
	
	public int add (int num1, int num2) {
		return num1 + num2;
	}
	
	public int add (int num1, int num2) {
		return num1 - num2;
	}
	
	public int substract(int num1, int num2) {
		return num1 * num2;
	}
	
	public int divide (int num1, int num2) {
		num1 / num2;
	}
	
	
}
